package co.com.codesoftware.persistencia.utilities;

public class RespuestaFacturacion {

	private String respuesta;
	private String trazaExcepcion;
	private String idFacturacion;

	public String getRespuesta() {
		return respuesta;
	}

	public void setRespuesta(String respuesta) {
		this.respuesta = respuesta;
	}

	public String getTrazaExcepcion() {
		return trazaExcepcion;
	}

	public void setTrazaExcepcion(String trazaExcepcion) {
		this.trazaExcepcion = trazaExcepcion;
	}

	public String getIdFacturacion() {
		return idFacturacion;
	}

	public void setIdFacturacion(String idFacturacion) {
		this.idFacturacion = idFacturacion;
	}

}
